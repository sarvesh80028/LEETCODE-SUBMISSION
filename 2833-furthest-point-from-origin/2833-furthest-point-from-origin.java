class Solution {
    public int furthestDistanceFromOrigin(String moves) {
        int l=0,r=0,k=0;
        for(int i=0;i<moves.length();i++){
            if(moves.charAt(i)=='L') l++;
            else if(moves.charAt(i)=='_') k++;
            else r++;
        }
        if(l>r) l+=k;
        else r+=k;
        return Math.abs(l-r);
    }
}